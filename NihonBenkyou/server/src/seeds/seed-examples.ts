// ─── Exported seed function ──────────────────────────────────────
export async function seedExamples(prisma: PrismaClient): Promise<void> {
  let updated = 0;
  let skipped = 0;

  for (const [word, { example, meaning }] of Object.entries(EXAMPLES)) {
    const result = await prisma.vocabulary.updateMany({
      where: { word },
      data: { example, exampleMeaning: meaning },
    });
    if (result.count > 0) {
      updated += result.count;
    } else {
      skipped++;
    }
  }

  const remaining = await prisma.vocabulary.count({ where: { example: '' } });
  console.log(`  ✓ examples: updated ${updated} rows (${skipped} skipped, ${remaining} still empty)`);
}

// ─── Standalone runner ────────────────────────────────────────────
async function main() {
  const adapter = new PrismaPg(process.env.DATABASE_URL!);
  const prisma = new PrismaClient({ adapter });
  console.log('[seed-examples] Starting...');
  await seedExamples(prisma);
  await prisma.$disconnect();
}

